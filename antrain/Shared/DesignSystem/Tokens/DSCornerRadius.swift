import Foundation

/// Design System Corner Radius
/// Consistent corner radius values for UI elements
struct DSCornerRadius {
    /// 4pt - Small elements (tags, badges)
    static let sm: CGFloat = 4

    /// 8pt - Buttons, text fields
    static let md: CGFloat = 8

    /// 12pt - Cards
    static let lg: CGFloat = 12

    /// 16pt - Large cards, modals
    static let xl: CGFloat = 16

    /// 9999pt - Pills, circular buttons
    static let full: CGFloat = 9999
}
